#La fonction
def count_word():
#La phrase où les mots vont êtres countés
    word=("Une belle phrase")
#compte le nombre d`espaces et ajoute 1 pour avoir le nombre de mots
    wordcount=(word.count(" ")+1)
#montre le nombre de mots
    print(wordcount)
#appel la fonction
count_word()
